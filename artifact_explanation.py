from PIL import Image
from ovis_inference import run_ovis_inference

def artifact_explainer(img_path, object_class, predicted_artifact_list, num_of_artifacts, ovis, text_tokenizer, visual_tokenizer, prediction_label):
    """
    Generates explanations that are specific to the detected object (e.g., Horse).
    """

    image = Image.open(img_path)
    
    # Instruction based on Real/Fake prediction
    if prediction_label == "Fake":
        status_msg = f"This image of a {object_class} is FAKE. Explain the digital errors."
    else:
        status_msg = f"This image of a {object_class} is REAL. Explain the natural photographic details."

    ovis_prompt = f"""
      Forensic Task: Analyze the provided image of a {object_class}.
      Current Status: {status_msg}
      Detected Artifacts: {", ".join(predicted_artifact_list)}

      **Your Instructions**:
      Provide a specific explanation for each artifact. You MUST reference features of a {object_class} (such as the texture of the hair, the shape of the limbs, or the lighting on the body). 
      
      - If the artifact is 'Misshapen appendages' and the object is a 'Horse', describe how the hooves or legs look physically impossible.
      - If the artifact is 'Improper fur direction', describe the flow of the {object_class}'s coat or mane.
      - Ensure each explanation is context-aware and at least 30 words long.

      **Format**:
      - **Artifact Name**: [Name]
      - **Explanation**: [Detailed analysis of the {object_class} in the image]
      
      ---
      
      Example for a Horse (Fake):
      **Artifact Name**: Misshapen ears or appendages
      **Explanation**: The horse's front legs exhibit an impossible skeletal structure, appearing to merge into the ground without distinct hooves. This anatomical failure is a common limitation of AI generative models which struggle with the complex geometry of animal limbs, unlike the clear, defined anatomy found in a real horse.
      """
    
    ovis_output = run_ovis_inference(image, ovis_prompt, ovis, text_tokenizer, visual_tokenizer)
    return ovis_output